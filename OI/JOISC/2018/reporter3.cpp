void solve(){
    int n,q;cin >> n >> q;
    vector<int> d(n+1);
    for(int i=1;i<=n;i++) cin >> d[i];
    int cur=d[1],l=1,r=0;
    vector<tuple<int,int,int>> p;
    while(l<=n){
        while(r+1<=n && d[r+1]<=cur) r++;
        p.push_back({cur,l,r});l=r+1;
        if(l==n+1) break;
        cur*=((d[l]-1)/cur+1);
    }
    for(int i=1;i<=q;i++){
        int t,ans=0;cin >> t >> l >> r;
        ans+=(l<=t && t<=r);
        for(auto &[x,lt,rt]:p){
            int add=(t/x)*x;
            ans+=max(0LL,min(r,-lt+add)-max(l,-rt+add)+1);
            cur*=2;
        }
        cout << ans << '\n';
    }
}

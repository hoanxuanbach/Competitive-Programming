void solve(){
    int n,m;
    pii ans={0,-1};
    cin >> n >> m;m=min(m,n);
    auto cal = [&](int x){
        int cur=1,d=x,res=0;
        for(int i=1;i<=x;i++){
            int num=(n-d)/x+1,k;
            if(cur<d) k=0;
            else k=(cur-d)/(x-1)+1;
            if(k<num) res+=d*(num-k)+(num-k)*(k+num-1)*x/2;
            cur+=num;d=(d+num*x-1)%n+1;
        }
        return res;
    };
    for(int i=2;i<=m;i++){
        int g=__gcd(i,n);
        if(g==i) ans=max(ans,{n*(n/i+1)/2,-i});
        else if(g==1) ans=max(ans,{cal(i),-i});
    }
    cout << -ans.se << '\n';
}

